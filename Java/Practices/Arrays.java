public class Arrays {
    public static void main(String[] args)  {
       int marks[] = new int[6];
       marks[0] = 98;
       marks[1] = 87;
       marks[2] = 79;
       marks[3] = 85;
       System.out.println("marks[0]");
       System.out.println("marks[1]");
       System.out.println("marks[2]");
       System.out.println("marks[3]");
       System.out.println("marks[4]");
       System.out.println("marks[5]"); 
    }
}
