package com.Collections;
import java.util.*;


public class hashtable {
    public static void main(String[] args) {
        Hashtable<Integer, String> Queens = new Hashtable<>();
        Queens.put(1, "Saran");
        Queens.put(2, "Abi");
        Queens.put(3, "Jeru");
        Queens.put(4, "Swetha");
        System.out.println("Queens : " + Queens);
    }
}
